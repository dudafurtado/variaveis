let d = 7, u = 9, dd = 31, a = 34;

let matematica = d + u;
console.log(matematica)
console.log(`O resultado da soma de ${d} e ${u} é ${matematica}`)

matematica = d - u;
console.log(matematica)
console.log(`O resultado da subtração de ${d} e ${u} é ${matematica}`)

matematica = dd + a;
console.log(matematica)
console.log(`O resultado da soma de ${dd} e ${a} é ${matematica}`)

matematica = dd - a;
console.log(matematica)
console.log(`O resultado da subtração de ${dd} e ${a} é ${matematica}`)

matematica = d * u;
console.log(matematica)
console.log(`O resultado da multiplicação de ${d} com ${u} é ${matematica}`)

matematica = dd * a;
console.log(matematica)
console.log(`O resultado da multiplicação de ${dd} com ${a} é ${matematica}`)

matematica = d / u;
console.log(matematica)
console.log(`O resultado da divisão de ${d} com ${u} é ${matematica}`)

matematica = dd / a;
console.log(matematica)
console.log(`O resultado da divisão de ${dd} com ${a} é ${matematica}`)

// os simbolos para trabalhar com a matemática de números no javascript são :
// + para soma, - para subtração, / para divisão, * para multiplicação