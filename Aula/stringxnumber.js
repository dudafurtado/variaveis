let matematica = 18 + 2
console.log(matematica)
// Quando somamos um valor númerico com outro de mesmo tipo, a lógica é relacionada a matématica
// Então 2 + 2 retornaria o resultado 4

let matematica2 = "18" + 2
// Quando utilizamos valor string somado a um valor número o resultado é a concatenação
// Ou seja, "18" + 2 = 182
console.log(matematica2)